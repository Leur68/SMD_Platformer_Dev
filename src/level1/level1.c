void stateLevel1_environmentInit(u8* collisions) {
    for (s16 x = 0; x < MAP_WIDTH_TILES; x++) {
        for (s16 y = 0; y < MAP_HEIGHT_TILES; y++) {
            u8 objType = masPointer2(collisions, y, x, MAP_WIDTH_TILES);
            if (objType > 1) {
                s16 globalPosX = (x << 3);
                s16 globalPosY = (y << 3);

                objects[lastLoadedObject] = (GameObject*) MEM_alloc(sizeof(GameObject));
                objects[lastLoadedObject]->globalPos.x = globalPosX;
                objects[lastLoadedObject]->globalPos.y = globalPosY;
                objects[lastLoadedObject]->objType = objType;

                SpriteDefinition *spriteDef;
                if (objType == OBJECT_BONUS) {
                    spriteDef = &smth_sprite;
                }

                objects[lastLoadedObject]->sprite = SPR_addSpriteSafe(spriteDef, globalPosX - cameraPosition.x, globalPosY - cameraPosition.y, TILE_ATTR_FULL(PLAYER_PALETTE, 0, false, false, 0));
                objects[lastLoadedObject]->visible = true;
                
                lastLoadedObject++;
            }
        }
    }
}

void stateLevel1_environmentUpdate() {
    for (s16 x = 0; x < lastLoadedObject; x++) {
        GameObject *currObject = objects[x];

        if (currObject == NULL || player->isMoving == false) {
            continue;
        }

        s16 newPosX = currObject->globalPos.x - cameraPosition.x;
        s16 newPosY = currObject->globalPos.y - cameraPosition.y;

        if ((newPosX + 8) < 0 || newPosX > SCREEN_WIDTH || newPosY < 0 || newPosY > SCREEN_HEIGHT) {
            // Если спрайт вышел за пределы экрана, временно удаляем его из памяти
            if (currObject->visible == true) {
                SPR_releaseSprite(currObject->sprite);
                currObject->visible = false;
            }
        } else {
            // Если спрайт появился в зоне видимости, но ранее был скрыт, восстанавливаем
            if (currObject->visible == false) {
                // Выбираем спрайт для текущего типа объекта
                SpriteDefinition *spriteDef;
                if (currObject->objType == OBJECT_BONUS) {
                    spriteDef = &smth_sprite;
                }

                currObject->sprite = SPR_addSpriteSafe(spriteDef, newPosX, newPosY, TILE_ATTR_FULL(PLAYER_PALETTE, 0, false, false, 0));
                currObject->visible = true;
            }


            // Проверяем спрайт на столкновение с персонажем
            AABB objAABB;
            objAABB.x.min = currObject->globalPos.x;
            objAABB.x.max = currObject->globalPos.x + 8;
            objAABB.y.min = currObject->globalPos.y;
            objAABB.y.max = currObject->globalPos.y + 8;

            if (engine_isOverlappingAABBs(player->globalAABB, objAABB)) {
                // Если столкновение произошло, удаляем объект безвозвратно и инкрементим игровой счет
                SPR_releaseSprite(currObject->sprite);
                MEM_free(currObject);
                objects[x] = NULL;
                stateLevel1_updateScore(score + 1);
            } else {
                // Если столкновения не было, обновляем позицию спрайта на экране с учетом перемещения камеры
                SPR_setPosition(currObject->sprite, newPosX, newPosY);
            }
        }
    }


    // engine_drawDebugInt("p", newPosX, 32, 18);
}

