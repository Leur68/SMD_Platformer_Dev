void stateLevel1_onUpdateObject() {
    switch (currObject->objType) {
        case M_PLATFORM_TILE_INDEX:
            if (currObject->shift < 96) {
                currObject->shift++;
            } else {
                currObject->dir = !currObject->dir;
                currObject->shift = 1; // На первой итерации должно быть 1
            }

            s16 shift = 0;
            if (currObject->dir) {
                currObject->globalAABB.x.min++;
                currObject->globalAABB.x.max++;

                // Вычисляем доп. смещение в пределах одного тайла, которое происходит при движении платформы, чтобы плавно, а не потайлово расчитывать коллизию персонажа с этим объектом
                shift = currObject->globalAABB.x.min - engine_roundDownByEight(currObject->globalAABB.x.min);
            }
            if (!currObject->dir) {
                currObject->globalAABB.x.min--;
                currObject->globalAABB.x.max--;

                // Вычисляем доп. смещение в пределах одного тайла, которое происходит при движении платформы, чтобы плавно, а не потайлово расчитывать коллизию персонажа с этим объектом
                shift =  currObject->globalAABB.x.min - engine_roundUpByEight(currObject->globalAABB.x.min);
            }




            s16 x = currObject->globalAABB.x.min >> 3;
            s16 y = currObject->globalAABB.y.min >> 3;
            for (u8 j = 0; j < 3; j++) {
                collisions1[y][x + j] = M_PLATFORM_TILE_INDEX + shift;
            }
            if (currObject->dir) {
                collisions1[y][x - 1] = 0;
            }
            if (!currObject->dir) {
                collisions1[y][x + 3] = 0;
            }
            break;
    }
}

